package com.ohgiraffers.section01.arithmetic;

public class Application1
{
    public static void main(String[] args)
    {
        /*산술연산자
        * 
        * 우선순위 곱하기, 같은경우 연산자의 결합방향에따라 실행순서 결정
        * */
        int num1=20;
        int num2=7;

        System.out.println(num1+num2);
        System.out.println(num1-num2);
        System.out.println(num1*num2);
        System.out.println(num1/num2);
        System.out.println(num1%num2);
    }
}
